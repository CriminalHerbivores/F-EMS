function go_profsrInsert() {
	var pr_Pw = document.formm.pr_Pw.value;
	var pr_Pw_on = document.formm.pr_Pw_on.value;
	if(pr_Pw == pr_Pw_on){
		document.formm.submit();
	}
}

function go_profsrUpdate() {
	var pr_Pw = document.formm.pr_Pw.value;
	var pr_Pw_on = document.formm.pr_Pw_on.value;
	if(pr_Pw == pr_Pw_on){
		document.formm.submit();
	}
}